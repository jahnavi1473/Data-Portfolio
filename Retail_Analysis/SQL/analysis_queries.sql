/* ============================================================
1. TOTAL REVENUE
Purpose:
Calculate total revenue generated from all sales transactions.
============================================================ */

SELECT
SUM(revenue) AS total_revenue
FROM fact_sales;  --- 10062821.423



/* ============================================================
2. TOTAL ORDERS
Purpose:
Count how many unique orders (invoices) were placed.
============================================================ */

SELECT
COUNT(DISTINCT InvoiceNo) AS total_orders
FROM fact_sales; ---24156



/* ============================================================
3. TOTAL UNITS SOLD
Purpose:
Calculate total quantity of products sold.
============================================================ */

SELECT
SUM(Quantity) AS total_units_sold
FROM fact_sales; --- 5381202



/* ============================================================
4. TOTAL CUSTOMERS
Purpose:
Count number of unique customers who made purchases.
============================================================ */

SELECT
COUNT(DISTINCT CustomerID) AS total_customers
FROM fact_sales
WHERE CustomerID IS NOT NULL; ---4363



/* ============================================================
5. AVERAGE ORDER VALUE
Purpose:
Measure how much revenue is generated per order on average.
============================================================ */

SELECT
SUM(revenue) / COUNT(DISTINCT InvoiceNo) AS average_order_value
FROM fact_sales; ---416.5764788458354032



/* ============================================================
6. MONTHLY REVENUE TREND
Purpose:
Analyze how revenue changes month by month.
Useful for identifying seasonality and growth patterns.
============================================================ */

SELECT
d.year,
d.month,
SUM(f.revenue) AS monthly_revenue
FROM fact_sales f
JOIN dim_date d
ON f.order_date = d.order_date
GROUP BY d.year, d.month
ORDER BY d.year, d.month;

